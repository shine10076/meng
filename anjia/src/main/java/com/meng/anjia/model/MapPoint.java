package com.meng.anjia.model;

/**
 * @author shine10076
 * @date 2019/3/28 16:12
 */
public class MapPoint {

    private int name;
    private double lng;
    private double lat;
    private int price;

    public int getName() {
        return name;
    }

    public void setName(int name) {
        this.name = name;
    }

    public double getLng() {
        return lng;
    }

    public void setLng(double lng) {
        this.lng = lng;
    }

    public double getLat() {
        return lat;
    }

    public void setLat(double lat) {
        this.lat = lat;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }
}
